/**
 * Renders a brand SVG icon for known services, or returns null (letting parent fall back to Lucide).
 */
export default function BrandIcon({ name, className, size = 16 }: BrandIconProps) {
  // Try to resolve by node type first
  const brandKey = nodeTypeToBrand[name];
  const renderer = brandKey ? brandSVGs[brandKey] : brandSVGs[name];

  if (!renderer) return null;
  return <>{renderer({ className, size })}</>;
}

/**
 * Get the brand color for a node type
 */
export function getBrandColor(nodeType: string): string | undefined {
  const brandKey = nodeTypeToBrand[nodeType];
  return brandKey ? brandColors[brandKey] : brandColors[nodeType];
}

/**
 * Check if a brand icon exists for a node type
 */
export function hasBrandIcon(nodeType: string): boolean {
  const brandKey = nodeTypeToBrand[nodeType];
  return !!(brandKey ? brandSVGs[brandKey] : brandSVGs[nodeType]);
}
